def quadrado_numero(numero):
    """
    Função que retorna o quadrado de um número.
    :param numero: Número a ser elevado ao quadrado.
    :return: O quadrado do número.
    """
    return numero ** 2
  
def quadrado_numerov2(numero):
    """
    Função que retorna o quadrado de um número.
    :param numero: Número a ser elevado ao quadrado.
    :return: O quadrado do número.
    """
    return numero * numero
  
print(quadrado_numero(5))  # Saída: 25
print(quadrado_numerov2(5))  # Saída: 25